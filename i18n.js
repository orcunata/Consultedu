let currentLang = localStorage.getItem('lang') || 'tr';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;

    const t = translations[lang];

    // Update text content elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            // Check if the translation contains HTML (spans, etc.)
            if (t[key].includes('<')) {
                el.innerHTML = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update HTML content elements (always set as innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (t[key] !== undefined) {
            el.innerHTML = t[key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key] !== undefined) {
            el.placeholder = t[key];
        }
    });

    // Update page title
    document.title = lang === 'tr'
        ? 'Pusula - Yurt Disindan Egitim Rehberiniz'
        : 'Pusula - Your Guide to Studying Abroad from Turkey';

    // Update meta description
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) {
        metaDesc.content = lang === 'tr'
            ? 'Yurt disinda okumak isteyen Turk ogrenciler icin yapay zeka destekli egitim danismanligi. Program secimi, vize, sigorta ve daha fazlasi icin kisisellestirilmis rehberlik.'
            : 'AI-powered educational consulting for Turkish students wanting to study abroad. Get personalized guidance on programs, visas, insurance, and more.';
    }

    // Update language toggle button
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.querySelector('.lang-flag').textContent = lang === 'tr' ? 'EN' : 'TR';
    }
}

function getToastMessage(key) {
    return translations[currentLang][key] || key;
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language
    setLanguage(currentLang);

    // Language toggle
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            setLanguage(currentLang === 'tr' ? 'en' : 'tr');
        });
    }
});
